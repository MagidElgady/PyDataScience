# This lesson looks at the 3 basic functions used in the SciPy library to learn more
# about different functions: help, info and source.

# Help function
# Requires specific function to be
# imported already
from scipy import cluster

# help returns info about any function
# help(cluster)


# Info function
# Info function returns info about any function
# but uses a different syntax
import scipy
scipy.info(cluster)

# source function
# Source function only returns the code for
# objects written in Python
scipy.source(cluster)
